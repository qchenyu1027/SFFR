class GaussianKernelLayer(nn.Module):
    def __init__(self, in_dim, out_dim, num_nodes=64, scales=[0.1, 0.5, 1.0]):
        super().__init__()
        self.num_nodes = num_nodes
        self.num_scales = len(scales)

        # 可学习参数
        self.centers = nn.Parameter(torch.randn(num_nodes))  # 节点中心 c_i
        self.weights = nn.Parameter(torch.randn(num_nodes, self.num_scales))  # 多尺度权重 w_{i,s}
        self.sigma = nn.Parameter(torch.tensor(scales), requires_grad=False)  # 固定σ

        # 线性变换
        self.proj_in = nn.Linear(in_dim, num_nodes)
        self.proj_out = nn.Linear(num_nodes, out_dim)

    def forward(self, x):
        # 输入投影 [B, D] -> [B, num_nodes]
        x = self.proj_in(x)

        # 计算多尺度高斯响应
        x = x.unsqueeze(-1)  # [B, num_nodes, 1]
        centers = self.centers.view(1, self.num_nodes, 1)  # [1, num_nodes, 1]
        diff = (x - centers) ** 2  # [B, num_nodes, 1]

        # 高斯核计算 [B, num_nodes, num_scales]
        gauss = torch.exp(-diff / (2 * self.sigma ** 2 + 1e-6))

        # 多尺度加权求和 [B, num_nodes]
        out = (gauss * self.weights.softmax(dim=-1)).sum(dim=-1)

        return self.proj_out(out)


