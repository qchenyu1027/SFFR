class GaussianReLUKANLayer(nn.Module):


    def __init__(self, input_size: int, g: int,  output_size: int,k: int=3, sigma: float = 0.5, train_ab: bool = True):
        """
        Parameters:
        input_size (int): Number of input feature channels
        g (int): Number of grid intervals
        k (int): Extra grid size for activation range
        output_size (int): Number of output feature channels
        sigma (float): Standard deviation for Gaussian RBF
        train_ab (bool): Whether to allow trainable phase parameters
        """
        super().__init__()
        self.g = g
        self.k = k
        self.r = 4 * g * g / ((k + 1) * (k + 1))
        self.sigma = sigma
        self.input_size = input_size
        self.output_size = output_size

        # Define grid phase parameters
        phase_low = np.arange(-k, g) / g
        phase_height = phase_low + (k + 1) / g

        # Create trainable phase parameters with correct shape
        # Changed shape to match the broadcasting requirements
        self.phase_low = nn.Parameter(
            torch.Tensor(phase_low).unsqueeze(0).repeat(input_size, 1),
            requires_grad=train_ab
        )
        self.phase_height = nn.Parameter(
            torch.Tensor(phase_height).unsqueeze(0).repeat(input_size, 1),
            requires_grad=train_ab
        )

        # Modified 2D convolution to maintain spatial dimensions
        self.equal_size_conv = nn.Conv2d(
            in_channels=input_size * (g + k),  # Modified to match the actual number of channels
            out_channels=output_size,
            kernel_size=1,  # Changed to 1x1 convolution
            padding=0
        )


    def gaussian_rbf(self, x, center):
        """
        Apply Gaussian RBF function
        x: input tensor
        center: center point
        """
        return torch.exp(-((x - center) ** 2) / (2 * self.sigma ** 2))


    def forward(self, x):
        """
        Forward pass
        Input: (batch_size, channels, height, width)
        Output: (batch_size, output_size, height, width)
        """
        B, C, H, W = x.shape

        # Reshape input for processing
        x = x.permute(0, 2, 3, 1)  # B,H,W,C
        x = x.reshape(-1, C)  # (B*H*W),C

        # Expand x for broadcasting
        x = x.unsqueeze(-1)  # (B*H*W),C,1

        # ReLU activation processing with proper broadcasting
        x1 = torch.relu(x - self.phase_low.unsqueeze(0))  # (B*H*W),C,(g+k)
        x2 = torch.relu(self.phase_height.unsqueeze(0) - x)  # (B*H*W),C,(g+k)
        relu_part = x1 * x2 * self.r

        # Gaussian RBF processing
        centers = (self.phase_low + self.phase_height) / 2
        gaussian_part = self.gaussian_rbf(x, centers.unsqueeze(0))

        # Combine ReLU and gaussian output
        combined = relu_part + gaussian_part

        # Normalization
        combined = combined * combined
        # print(f"combined0", combined.shape)
        # Reshape for convolution
        combined = combined.reshape(B, H, W, C * (self.g + self.k))  # B,H,W,C*(g+k)

        combined = combined.permute(0, 3, 1, 2)  # B,C*(g+k),H,W   1 1048 1616
        # print(f"combined1", combined.shape)
        # Apply convolution
        # output = self.equal_size_conv(combined)  # B,output_size,H,W
        # print(f"combined2", output.shape)
        return combined

class GSConv(nn.Module):
    # GSConv https://github.com/AlanLi1997/slim-neck-by-gsconv
    def __init__(self, c1, c2, k=1, s=1, g=1, act=True):
        super().__init__()
        c_ = c2 // 2
        self.cv1 = Conv(c1, c_, k, s, None, g, act)
        self.cv2 = Conv(c_, c_, 5, 1, None, c_, act)

    def forward(self, x):
        x1 = self.cv1(x)
        x2 = torch.cat((x1, self.cv2(x1)), 1)
        # shuffle
        b, n, h, w = x2.data.size()
        b_n = b * n // 2
        y = x2.reshape(b_n, 2, h * w)
        y = y.permute(1, 0, 2)
        y = y.reshape(2, -1, n // 2, h, w)

        return torch.cat((y[0], y[1]), 1)


